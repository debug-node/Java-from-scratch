package basic;

public class AddTwoNumbers {
    public static void main(String[] args) {
        // Declare and initialize two numbers
        int num1 = 25;
        int num2 = 30;

        // Add the numbers
        int sum = num1 + num2;

        // Display the result
        System.out.println("First Number: " + num1);
        System.out.println("Second Number: " + num2);
        System.out.println("Sum: " + sum);
    }
}

